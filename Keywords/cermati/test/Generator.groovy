public class Generator {
	@Keyword
	public String generateEmail(String defaultString, String mailAddress) {
		String randomString = RandomStringUtils.randomAlphabetic(10);	
		
		return defaultString + randomString + mailAddress;
	}
	
	@Keyword
	public String generatePhoneNumber(String prefix) {
		String randomDigits = RandomStringUtils.randomNumeric(8);
		
		return prefix + randomDigits;
	}
}
